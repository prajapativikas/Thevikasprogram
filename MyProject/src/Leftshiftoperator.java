public class Leftshiftoperator 
{
	public static void main(String args[])  
	{
		int a=25,b=10;
		a=a<<2;   // Leftshiftoperator 
		System.out.println(a);
		a=a>>2;  // Rightshiftoperator
		System.out.println(a);
		a=a>>2;  // Rightshiftoperator
		System.out.println(a);
		b=b<<2;
		System.out.println(b);
		b=b>>2;
		System.out.println(b);
		b=b>>2;
		System.out.println(b);
	}
}
