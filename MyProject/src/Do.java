public class Do 
{

	public static void main(String[] args) 
	{
	 int a=0;
	 while(a<=-1)
	 {
		 System.out.println(a);
		 a++;
	 }
	  
	 System.out.println("------------------------------------------------");
	
	 int b=0;
	 do
	 {
		 System.out.println(b);
		 b++;
	 }
	    while(b<=-1);  
	}
}
